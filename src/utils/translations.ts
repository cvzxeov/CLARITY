export const translateCategories = (categories: Record<string, string[]>, oldLang: Language, newLang: Language) => {
  const updated: Record<string, string[]> = {};
  for (const [key, extensions] of Object.entries(categories)) {
    let translatedKey = key;
    for (const catKey of CATEGORY_KEYS) {
      if (catKey === 'misc') continue; // fallback is virtual and handled separately
      const oldTranslations = Object.values(CATEGORY_TRANSLATIONS[catKey]);
      const matched = oldTranslations.some(t => t.toLowerCase() === key.toLowerCase());
      if (matched) {
        translatedKey = CATEGORY_TRANSLATIONS[catKey][newLang];
        break;
      }
    }
    updated[translatedKey] = extensions;
  }
  return updated;
};

