let currentLang = 'en';

// Elements
const langToggle = document.getElementById('lang-toggle');
const themeToggle = document.getElementById('theme-toggle');
const copyBtn = document.getElementById('copy-btn');
const cryptoKey = document.getElementById('crypto-key');
const logoImg = document.getElementById('logo-img');
const mockupLogoImg = document.getElementById('mockup-logo-img');
const body = document.body;

// Update UI based on language
function updateLanguage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[currentLang][key]) {
            // Keep inner span or just text
            if (el.children.length === 0 || el.tagName.toLowerCase() === 'span' || el.tagName.toLowerCase() === 'h3') {
                el.innerHTML = i18n[currentLang][key];
            } else if (el.tagName.toLowerCase() === 'a' && el.querySelector('svg')) {
                const svg = el.querySelector('svg').outerHTML;
                el.innerHTML = `<span>${i18n[currentLang][key]}</span>${svg}`;
            } else if (key === 'footerText') {
                el.innerHTML = i18n[currentLang][key];
            } else {
                el.textContent = i18n[currentLang][key];
            }
        }
    });
    langToggle.textContent = currentLang === 'en' ? 'RU' : 'EN';
}

// Event Listeners
langToggle.addEventListener('click', () => {
    currentLang = currentLang === 'en' ? 'ru' : 'en';
    updateLanguage();
});

function updateLogos() {
    const isLight = body.classList.contains('light-theme');
    logoImg.src = isLight ? 'src/img/logo_white.png' : 'src/img/logo_white.png';
    if (mockupLogoImg) mockupLogoImg.src = isLight ? 'src/img/logo_black.png' : 'src/img/logo_black.png';
}

// Initial call to set correct logos
updateLogos();

themeToggle.addEventListener('click', () => {
    body.classList.toggle('light-theme');
    updateLogos();
});

copyBtn.addEventListener('click', () => {
    navigator.clipboard.writeText(cryptoKey.textContent).then(() => {
        const originalText = copyBtn.textContent;
        copyBtn.textContent = currentLang === 'en' ? 'COPIED!' : 'СКОПИРОВАНО!';
        setTimeout(() => {
            copyBtn.textContent = originalText;
        }, 2000);
    });
});

// Scroll Reveal Animations using IntersectionObserver
const revealElements = document.querySelectorAll('.reveal');

const revealCallback = (entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('in-view');
            observer.unobserve(entry.target);
        }
    });
};

const revealOptions = {
    root: null,
    rootMargin: '0px 0px -100px 0px',
    threshold: 0.1
};

const revealObserver = new IntersectionObserver(revealCallback, revealOptions);

revealElements.forEach(el => {
    revealObserver.observe(el);
});

// Accordion Logic
const btnMores = document.querySelectorAll('.btn-more');
btnMores.forEach(btn => {
    btn.addEventListener('click', () => {
        const item = btn.closest('.feature-item');
        const details = item.querySelector('.feature-details');
        const isOpen = details.classList.contains('open');

        if (isOpen) {
            details.classList.remove('open');
            btn.textContent = i18n[currentLang]['learnMoreBtn'];
        } else {
            details.classList.add('open');
            btn.textContent = i18n[currentLang]['closeBtn'];
        }
    });
});

